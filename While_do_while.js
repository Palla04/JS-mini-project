// While loop

let i=0
while (i <= 10) {
    console.log(`value of i is ${i}`);
    i++
}

let Array = ["flash", "batman", "superman"]

let arr = 0
while(arr<Array.length){
    console.log(`Array[${arr}] = ${Array[arr]}`);
    arr++
}

// do-while Loop

let s=11;
do{
    console.log(`Score is ${s}`);  //just run for one time without checking the condition
    s++
}while(s<=10);